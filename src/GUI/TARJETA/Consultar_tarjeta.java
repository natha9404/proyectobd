    private void boton_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_buscarActionPerformed
        int id_tarjeta;
        
        id_tarjeta=Integer.parseInt(campo_id_tarjeta.getText());
        
        Tarjeta t;

        t=controladorTarjeta.consultarTarjeta(id_tarjeta);
        //saldo=(int) (t.getSaldo());
        
        campo_saldo.setText(Double.toString(t.getSaldo()));
        combo_estado.setSelectedItem(t.getEstado());
        campo_deuda.setText(Double.toString(t.getDeuda()));
        campo_vendedor.setText(t.getCedula_empleado());
        campo_estacion.setText(t.getNombre_estacion());
        
    }//GEN-LAST:event_boton_buscarActionPerformed

    private void boton_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_modificarActionPerformed
        if(boton_modificar.getText()=="Modificar"){
            campo_saldo.setEnabled(true);
            combo_estado.setEnabled(true);
            campo_deuda.setEnabled(true);
            campo_vendedor.setEnabled(true);
            campo_estacion.setEnabled(true);
            boton_modificar.setText("Guardar Cambios");
        }else if(boton_modificar.getText()=="Guardar Cambios"){
            int id_tarjeta;
            double saldo,deuda;
            String estado,estacion,vendedor;
            
            id_tarjeta=Integer.parseInt(campo_id_tarjeta.getText());
            saldo=Double.parseDouble(campo_saldo.getText());
            deuda=Double.parseDouble(campo_deuda.getText());
            estado=(String)combo_estado.getSelectedItem();
            vendedor=campo_vendedor.getText();
            estacion=campo_estacion.getText();
        
            Tarjeta t=new Tarjeta(id_tarjeta,saldo,estado,deuda,vendedor,estacion);
        
            int numFilas = controladorTarjeta.modificarTarjeta(t);
        
            System.out.println ("Filas "+ numFilas);
            if (numFilas == 1){
            
                JOptionPane.showMessageDialog(null, "Tarjeta modificada exitosamente");
            }
            else {
                JOptionPane.showMessageDialog(null, "Ocurrio un problema al modificar la tarjeta");
            }
            modelo=controladorTarjeta.listarTarjetas();
            this.tabla_tarjetas.setModel(modelo);
            
            boton_modificar.setText("Modificar");
            campo_saldo.setEnabled(false);
            combo_estado.setEnabled(false);
            campo_deuda.setEnabled(false);
            campo_vendedor.setEnabled(false);
            campo_estacion.setEnabled(false);
        }
    }//GEN-LAST:event_boton_modificarActionPerformed

    private void boton_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_eliminarActionPerformed
        int id_tarjeta;
        id_tarjeta=Integer.parseInt(campo_id_tarjeta.getText());
        //confirmacion=JOptionPane.showMessageDialog(null, "Esta seguro que desea eleminar el bus "+placa+"");
        int confirmacion=JOptionPane.showConfirmDialog(this, "Esta seguro que desea eleminar la tarjeta "+id_tarjeta);
        if(confirmacion==0){
            controladorTarjeta.eliminarTarjeta(id_tarjeta);
            campo_id_tarjeta.setText("");
            campo_saldo.setText("");
            campo_deuda.setText("");
            campo_vendedor.setText("");
            campo_estacion.setText("");
            
            modelo=controladorTarjeta.listarTarjetas();
            this.tabla_tarjetas.setModel(modelo);
            JOptionPane.showMessageDialog(null, "Se ha eliminado la tarjeta "+id_tarjeta+" exitosamente.");
        }
    }//GEN-LAST:event_boton_eliminarActionPerformed

