    private void boton_recargar_tarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_recargar_tarjetaActionPerformed
        int id_tarjeta;
        double valor_recarga;
        id_tarjeta=Integer.parseInt(campo_id_tarjeta.getText());
        valor_recarga=(double)Integer.parseInt(campo_recarga.getText());
        int resultado=controladorTarjeta.recargarTarjeta(id_tarjeta, valor_recarga);
        if(resultado==1){
            JOptionPane.showMessageDialog(null, "La recarga ha sido exitosa.");
        }else{
            JOptionPane.showMessageDialog(null, "Ocurrio un problema con el recarga.");
        }
        this.dispose();
        
    }//GEN-LAST:event_boton_recargar_tarjetaActionPerformed

