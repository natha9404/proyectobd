    private void boton_vender_tarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_vender_tarjetaActionPerformed
        double saldo,deuda;
        String estado,empleado_vendedor,nombre_estacion;
        
        saldo=(double)Integer.parseInt(campo_saldo.getText());
        deuda=0;
        
        estado=campo_estado.getText();
        empleado_vendedor=campo_vendedor.getText();
        nombre_estacion=campo_estacion.getText();
        
        Tarjeta t =new Tarjeta();
        t.setSaldo(saldo);
        t.setDeuda(deuda);
        t.setEstado(estado);
        t.setCedula_empleado(empleado_vendedor);
        t.setNombre_estacion(nombre_estacion);
        
        int numfilas=controladorTarjeta.registrarTarjeta(t);
        
        if(numfilas==1){
            JOptionPane.showMessageDialog(null, "La tarjeta ha sido registrada con exito.");
            campo_saldo.setText("");
            campo_vendedor.setText("");
            campo_estacion.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Ocurrio un problema al registrar la tarjeta.");
        }
        
    }//GEN-LAST:event_boton_vender_tarjetaActionPerformed

